using System;

public class MathsFloat
{
    static public void Main()
    
    {   int number1 = 5;
        int number2 = 3;
        float total;

         // Divide integer numbers, giving a float result, with decimals.
        total = number1 / (float) number2;
   
      
        Console.WriteLine ("Total is {0}", total);

    }
}

// Total is 1,666667